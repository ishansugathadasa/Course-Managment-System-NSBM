    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try {
            String lecturerId = lecturerIdTxt.getText();
            int isDeleted = LecturerController.deleteLecturer(lecturerId);
            if(isDeleted>0){
                JOptionPane.showMessageDialog(this, "Lecturer Deleted sucsess...");
                refresh();
            }else{
                JOptionPane.showMessageDialog(this, "Somthing wrong...");
                refresh();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageLecturerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            String lecturerId = lecturerIdTxt.getText();
            String name = nameTxt.getText();
            String nic = nicTxt.getText();
            String email = emailTxt.getText();
            int phone = Integer.parseInt(phoneTxt.getText());
            String address = addressTxt.getText();
            String gender = genderTxt.getText();
            String dob = dobTxt.getText();
            
            Lecturer lecturer = new Lecturer(lecturerId, name, nic, email, phone, 
                    address, gender, dob);
            int isAdded = LecturerController.addLecturer(lecturer);
            if(isAdded>0){
                JOptionPane.showMessageDialog(this, "Lecturer Added sucsess...");
                newLecturerBtn.setVisible(true);
                addBtn.setVisible(false);
                lecturerIdCombo.setVisible(true);
                lecturerIdTxt.setVisible(false);
                refresh();
            }else{
                JOptionPane.showMessageDialog(this, "Somthing wrong...");
                newLecturerBtn.setVisible(true);
                addBtn.setVisible(false);
                lecturerIdCombo.setVisible(true);
                lecturerIdTxt.setVisible(false);
                refresh();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageLecturerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

