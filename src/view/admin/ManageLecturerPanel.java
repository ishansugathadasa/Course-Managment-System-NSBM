    private void fillLecturerIdCombo(){
        try {
            ArrayList<Lecturer> lecturers=LecturerController.getAllLecturer();
            for (Lecturer lecturer : lecturers) {
                lecturerIdCombo.addItem(lecturer.getLecturerId());
            }
            new ComboSearch().setSearchableCombo(lecturerIdCombo, true, "No Lecturer found");
            //new ComboSearch().setSearchableCombo(student_idCOmbo, true, "No Student found");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(RegisterUnderGraduateStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void refresh(){
        lecturerIdCombo.removeAllItems();
        fillLecturerIdCombo();
    }
