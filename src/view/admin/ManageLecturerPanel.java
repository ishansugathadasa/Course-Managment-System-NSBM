    private void lecturerIdComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_lecturerIdComboItemStateChanged
        try {
            if (lecturerIdCombo.getSelectedItem()!=null) {
                String lecturerId = lecturerIdCombo.getSelectedItem().toString();
                Lecturer lecturer=LecturerController.searchLecturerId(lecturerId);
                if (lecturer!=null) {
                    nameTxt.setText(lecturer.getName());
                    nicTxt.setText(lecturer.getNic());
                    emailTxt.setText(lecturer.getEmail());
                    phoneTxt.setText(Integer.toString(lecturer.getPhoneNo()));
                    addressTxt.setText(lecturer.getAddress());
                    genderTxt.setText(lecturer.getGender());
                    dobTxt.setText(lecturer.getDob());
                }else{
                    JOptionPane.showMessageDialog(this, "Somthing wrong...");
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageLecturerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lecturerIdComboItemStateChanged

