    private void fillCourseIdCombo(){
        try {
            ArrayList<Course> courses=CourseController.getAllCourse();
            for (Course course : courses) {
                courseIdCombo.addItem(course.getCourseId());
            }
            new ComboSearch().setSearchableCombo(courseIdCombo, true, "No Course found");
            //new ComboSearch().setSearchableCombo(student_idCOmbo, true, "No Student found");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(RegisterUnderGraduateStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void refresh(){
        courseIdCombo.removeAllItems();
        fillCourseIdCombo();
    }
