    private void courseIdComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_courseIdComboItemStateChanged
        try {
            if (courseIdCombo.getSelectedItem()!=null) {
               String courseId = courseIdCombo.getSelectedItem().toString();
                Course course=CourseController.searchCourseId(courseId);
                if (course!=null) {
                    nameTxt.setText(course.getName());
                }else{
                    JOptionPane.showMessageDialog(this, "Somthing wrong...");
                } 
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageCoursePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_courseIdComboItemStateChanged

