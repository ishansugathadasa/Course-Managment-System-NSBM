    private void deeteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deeteBtnActionPerformed
        try {
            String facultyId = facultyIdCombo.getSelectedItem().toString();
            int isDeleted=FacultyController.deleteFaculty(facultyId);
            if (isDeleted>0) {
                JOptionPane.showMessageDialog(this, "Faculty Deleted success...");
                refresh();
            }else{
                JOptionPane.showMessageDialog(this, "Somthing wrong...");
                refresh();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageFacultyPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deeteBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            String faculty = facultyIdTxt.getText();
            String name = nameTxt.getText();
            Faculty faculty1=new Faculty(faculty, name);
            int isAdded=FacultyController.addFaculty(faculty1);
            if (isAdded>0) {
                JOptionPane.showMessageDialog(this, "Faculty added success...");
                facultyIdCombo.setVisible(true);
                facultyIdTxt.setVisible(false);
                newCourseBtn.setVisible(true);
                addBtn.setVisible(false);
                refresh();
            }else{
                JOptionPane.showMessageDialog(this, "Somthing wrong...");
                facultyIdCombo.setVisible(true);
                facultyIdTxt.setVisible(false);
                newCourseBtn.setVisible(true);
                addBtn.setVisible(false);
                refresh();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageFacultyPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

