     private void fillFacultyCombo(){
        try {
            ArrayList<Faculty> faculties=FacultyController.getAllFaculty();
            for (Faculty faculty : faculties) {
                facultyIdCombo.addItem(faculty.getFacultyId());
            }
            new ComboSearch().setSearchableCombo(facultyIdCombo, true, "No Faculty found");
            //new ComboSearch().setSearchableCombo(student_idCOmbo, true, "No Student found");
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(RegisterUnderGraduateStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     
    private void refresh(){
        facultyIdCombo.removeAllItems();
        fillFacultyCombo();
    }
