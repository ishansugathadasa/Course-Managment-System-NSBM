    private void facultyIdComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_facultyIdComboItemStateChanged
        try {
            if (facultyIdCombo.getSelectedItem()!=null) {
                String facultyId = facultyIdCombo.getSelectedItem().toString();
                Faculty faculty=FacultyController.searchFacultyId(facultyId);
                if (faculty!=null) {
                    nameTxt.setText(faculty.getName());
                }else{
                    JOptionPane.showMessageDialog(this, "Somthing wrong...");
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageCoursePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_facultyIdComboItemStateChanged

